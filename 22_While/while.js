// While loop

let index = 0;
while (index <= 10) {
  console.log(`value od index ${index}`);
  index = index + 2;
}

let myArray = ["batman", "superman", "flash"];

let arr = 0;
while (arr < myArray.length) {
  console.log(`value is  ${myArray[arr]}`);
  arr = arr + 1;
}

// Do while looop

let score = 11;

do {
  console.log(`score is ${score}`);
  score++;
} while (score <= 10);

